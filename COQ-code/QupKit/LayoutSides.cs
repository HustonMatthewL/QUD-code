namespace QupKit
{
	public class LayoutSides
	{
		public float Top;

		public float Bottom;

		public float Left;

		public float Right;

		public LayoutSides()
		{
		}

		public LayoutSides(float Top, float Right, float Bottom, float Left)
		{
			this.Top = Top;
			this.Right = Right;
			this.Bottom = Bottom;
			this.Left = Left;
		}
	}
}
